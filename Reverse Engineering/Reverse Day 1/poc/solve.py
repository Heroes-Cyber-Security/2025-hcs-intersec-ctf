# Encrypted flag bytes from the binary
enc = [
    0x16, 0x1D, 0x0D, 0x25, 0x2C, 0x3B, 0x28, 0x3B,
    0x2C, 0x2D, 0x3B, 0x01, 0x37, 0x2D, 0x01, 0x3B,
    0x3F, 0x24, 0x27, 0x27, 0x27, 0x27, 0x23
]

# XOR key
key = 0x5E

# Decrypt the flag
flag = ""
for byte in enc:
    decrypted_char = chr(byte ^ key)
    flag += decrypted_char

print(f"Flag: {flag}")
